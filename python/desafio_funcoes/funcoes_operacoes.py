

''' ----------------------------soma: serve para somar dois números inteiros '''
def soma(numero1, numero2):
    return (f'a soma entre {numero1} + {numero2} = {numero1 + numero2}')


'''  ----------------------------subtração: serve para subtrair dois números inteiros '''

def subtracao(numero1, numero2):
    return (f'a subtração entre {numero1} - {numero2} = {numero1 - numero2}')


''' ----------------------------multiplicação: serve para multiplicar dois números inteiros '''

def multiplicacao(numero1, numero2):
    return (f'a multiplicação entre {numero1} x {numero2} = {numero1 * numero2}')


''' ----------------------------divisão: serve para dividir dois números inteiros '''

def divisao(numero1, numero2):
    return (f'a divisão entre {numero1} / {numero2} = {numero1 / numero2}')
    










